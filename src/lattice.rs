#[cfg(test)]
mod unit_tests {
    use super::*;

    #[test]
    fn d2q9() {
        let mut sum = [0, 0];
        for e in D2Q9 {
            for d in 0..2 {
                sum[d] += e[d];
            }
        }
        for s in sum {
            assert_eq!(s, 0);
        }
    }

    #[test]
    fn d3q27() {
        let mut sum = [0, 0, 0];
        for e in D3Q27 {
            for d in 0..3 {
                sum[d] += e[d];
            }
        }
        for s in sum {
            assert_eq!(s, 0);
        }
    }
}
